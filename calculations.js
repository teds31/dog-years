//age variable
let myAge = 31;

//two human years for dog year math
let earlyYears = 2;

//two human years * 10.5 for dog years
earlyYears = earlyYears * 10.5;

//account for two years from myAge
let laterYears = myAge - 2;

//dog years calculation
let dogYears = laterYears * 4;

//my age in dog years;
let myAgeInDogYears = earlyYears + laterYears;

//string variablef or name and change to lowercase
let myName = 'Teddy';
myName = myName.toLowerCase();

//log to console name with age in dog years
console.log(`My name is ${myName}. I am ${myAgeInDogYears} years old in dog years.`)
